package ru.stimmax.kotlin_course.lessons.lesson10.homework

fun main() {

    // 1. Создайте пустой неизменяемый словарь, где ключи и значения - целые числа.
    val emptyMap = mapOf<Int, Int>()
    println("1) $emptyMap")

    // 2. Создайте словарь, инициализированный несколькими парами "ключ-значение",
    // где ключи - float, а значения - double.
    val floatToDouble = mapOf(
        1.5f to 2.5,
        2.0f to 4.0,
        3.2f to 9.5
    )
    println("2) $floatToDouble")

    // 3. Создайте изменяемый словарь, где ключи - целые числа, а значения - строки.
    val mutableMap = mutableMapOf(
        1 to "один",
        2 to "два"
    )
    println("3) $mutableMap")

    // 4. Имея изменяемый словарь, добавьте в него новые пары "ключ-значение".
    mutableMap[3] = "три"
    mutableMap[4] = "четыре"
    println("4) $mutableMap")

    // 5. Используя словарь из предыдущего задания, извлеките значение, используя ключ.
    // Попробуй получить значение с ключом, которого в словаре нет.
    val value1 = mutableMap[2]
    val value2 = mutableMap[10]
    println("5) Значение для ключа 2: $value1")
    println("5) Значение для ключа 10: $value2")

    // 6. Удалите определенный элемент из изменяемого словаря по его ключу.
    mutableMap.remove(3)
    println("6) $mutableMap")

    // 7. Создайте словарь (ключи Double, значения Int) и выведи в цикле результат деления ключа на значение.
    // Не забудь обработать деление на 0 (в этом случае выведи слово “бесконечность”).
    val divMap = mapOf(
        10.0 to 2,
        5.0 to 0,
        8.0 to 4
    )

    println("7)")
    for ((key, value) in divMap) {
        if (value == 0)
            println("$key / $value = бесконечность")
        else
            println("$key / $value = ${key / value}")
    }

    // 8. Измените значение для существующего ключа в изменяемом словаре.
    mutableMap[2] = "два (обновлено)"
    println("8) $mutableMap")

    // 9. Создайте два словаря и объедините их в третьем изменяемом словаре через циклы.
    val mapA = mapOf(1 to "a", 2 to "b")
    val mapB = mapOf(3 to "c", 4 to "d")
    val merged = mutableMapOf<Int, String>()

    for ((k, v) in mapA) merged[k] = v
    for ((k, v) in mapB) merged[k] = v

    println("9) $merged")

    // 10. Создайте словарь, где ключами являются строки, а значениями - списки целых чисел.
    // Добавьте несколько элементов в этот словарь.
    val numbersByCategory = mutableMapOf(
        "четные" to listOf(2, 4, 6, 8),
        "нечетные" to listOf(1, 3, 5, 7)
    )
    println("10) $numbersByCategory")

    // 11. Создай словарь, в котором ключи - это целые числа, а значения - изменяемые множества строк.
    // Добавь данные в словарь. Получи значение по ключу (это должно быть множество строк)
    // и добавь в это множество ещё строку. Распечатай полученное множество.
    val setMap = mutableMapOf(
        1 to mutableSetOf("apple", "banana"),
        2 to mutableSetOf("carrot")
    )
    setMap[1]?.add("cherry")
    println("11) ${setMap[1]}")

    // 12. Создай словарь, где ключами будут пары чисел.
    // Через перебор найди значение, у которого пара будет содержать цифру 5
    // в качестве первого или второго значения.
    val pairMap = mapOf(
        (1 to 2) to "A",
        (5 to 3) to "B",
        (4 to 5) to "C",
        (7 to 8) to "D"
    )
    println("12)")
    for ((pair, value) in pairMap) {
        if (pair.first == 5 || pair.second == 5)
            println("Пара $pair содержит 5, значение: $value")
    }

    // ============================
    // Задачи на подбор типа словаря
    // ============================

    // Словарь библиотека: Ключи - автор книги, значения - список книг
    val library = mapOf(
        "Толстой" to listOf("Война и мир", "Анна Каренина"),
        "Достоевский" to listOf("Преступление и наказание", "Идиот")
    )
    println("Библиотека: $library")

    // Справочник растений: Ключи - типы растений (например, "Цветы", "Деревья"),
    // значения - списки названий растений
    val plants = mapOf(
        "Цветы" to listOf("Роза", "Тюльпан", "Лилия"),
        "Деревья" to listOf("Дуб", "Сосна", "Берёза")
    )
    println("Растения: $plants")

    // Четвертьфинала: Ключи - названия спортивных команд,
    // значения - списки игроков каждой команды
    val teams = mapOf(
        "Спартак" to listOf("Иванов", "Петров", "Сидоров"),
        "Зенит" to listOf("Смирнов", "Кузнецов", "Орлов")
    )
    println("Команды: $teams")

    // Курс лечения: Ключи - даты, значения - список препаратов принимаемых в дату
    val treatment = mapOf(
        "2025-10-10" to listOf("Аспирин", "Парацетамол"),
        "2025-10-11" to listOf("Амоксициллин")
    )
    println("Курс лечения: $treatment")

    // Словарь путешественника: Ключи - страны, значения - словари из городов со списком интересных мест
    val travel = mapOf(
        "Италия" to mapOf(
            "Рим" to listOf("Колизей", "Фонтан Треви"),
            "Венеция" to listOf("Гранд-канал", "Площадь Сан-Марко")
        ),
        "Франция" to mapOf(
            "Париж" to listOf("Эйфелева башня", "Лувр")
        )
    )
    println("Путешествия: $travel")
}
